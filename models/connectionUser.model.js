const mongoose = require('mongoose');

const ConnectionUserSchema = mongoose.Schema({
    id: {
        type: Number,
        require: true
    },
    firstName: {
        type: String,
        require: true
    },
    lastName: {
        type: String,
        require: true
    },
    email: {
        type: String,
        require: true
    },
    phone: {
        type: String,
        require: true
    },
    cellPhone: {
        type: String,
        require: true
    },
    userName: {
        type: String,
        require: true
    },
    password: {
        type: String,
        require: true
    },
    permission: {
        type: String,
        require: true
    }
})

module.exports = mongoose.model('ConnectionUser', ConnectionUserSchema)